"""scripts/gen_stubs.py.

Generate minimal typed stubs for ``dino_datasets`` and ``dino_env``.

These stubs cover exactly the symbols imported by ``dino_loader`` — no more.
They are written to ``stubs/dino_datasets.pyi`` and ``stubs/dino_env.pyi``
relative to the repository root.

Usage
-----
    python scripts/gen_stubs.py          # regenerate both stubs
    python scripts/gen_stubs.py --check  # exit 1 if stubs are out of date

Design
------
The generator uses ``inspect`` to reflect the *installed* packages rather than
parsing source, so it always tracks the version that is actually installed in
the current virtual environment.  Running ``just gen-stubs`` after ``uv sync``
guarantees the stubs stay in sync.

Only the public symbols listed in ``_DINO_DATASETS_SYMBOLS`` and
``_DINO_ENV_SYMBOLS`` are emitted.  Everything else is intentionally omitted
to keep the stubs minimal and context-window-friendly.
"""

